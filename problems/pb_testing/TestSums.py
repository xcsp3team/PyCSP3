from pycsp3 import *

x = VarArray(size=[2, 2], dom=range(150))
y = VarArray(size=[2, 2], dom=range(150))
z = VarArray(size=[2, 2], dom=range(150))
a = Var(dom=range(2))

satisfy(
    Sum(x * [1, 1, 2, 1]) > 2,
    Sum(x) + Sum(y) > 4,
    Sum(x * [1, 1, 2, 1]) + Sum(y * [1, 2, 1, 1]) > 4,
    Sum(x * [1, 1, 2, 1]) + Sum(y) > 4,
    Sum([x[0]] * [1, 2]) + Sum(y * [1, 2, 3, 4]) <= 2,
    Sum(x) + Sum(y * [1, 2, 3, 4]) <= 2,
    Sum(x) + Sum(y * [1, 2, 3, 4]) <= 2,
    Sum(x * [3, 3, 3, 3]) - Sum(y * [1, 2, 3, 4]) <= 2,
    Sum(x) - Sum(y * [1, 2, 3, 4]) <= 2,
    Sum(x) + 3 * y[0][0] >= 5,
    Sum(x * [3, 3, 3, 4]) + 5 * y[0][0] >= 5,
    Sum(x * [3, 3, 3, 4]) + y[0][0] * 6 >= 10,
    Sum(x) + 3 * z[0][0] + 2 * z[0][1] + 1 * z[1][1] >= 1000,
    Sum(x) + 3 * z[0][0] + 2 * z[0][1] + 1 * z[1][1] + z[1][0] >= 2000,
    Sum(x) + 2 * z[0][0] + 3 * z[0][1] + 1 * z[1][0] + z[1][1] >= 2000,
    3 * z[0][1] - Sum(x) < 10,
    3 * z[0][0] - Sum(x) + 2 * z[0][1] + 1 * z[1][1] - Sum(x) > 4,
    3 * z[0][0] - Sum(x * [9, 9, 9, 9]) + 2 * z[0][1] + 1 * z[1][1] - Sum(x) > 4,
    z[0][0] - Sum(x * [9, 9, 9, 9]) + 2 * z[0][1] + 1 * z[1][1] - Sum(x) > 4444,
    a - Sum(x * [9, 9, 9, 9]) + 2 * z[0][1] + 1 * z[1][1] - Sum(x) > 4444,
    a - Sum(x) + 2 * z[0][1] + 1 * z[1][1] - Sum(x) > 4444,
    a - Sum(x) + ([z[1][1], z[0][1]] * [1, 1]) - Sum(x) > 4444,
    3 * z[0][1] < 10
)
